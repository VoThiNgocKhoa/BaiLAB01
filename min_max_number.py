number1 = int(input("Nhập số thứ nhất: "))
number2 = int(input("Nhập số thứ hai: "))
number3 = int(input("Nhập số thứ ba: "))
max_number = max(number1, number2, number3)
min_number = min(number1, number2, number3)
print(f"Số lớn nhất là: {max_number}")
print(f"Số nhỏ nhất là: {min_number}")
